<script>
    $('.fontawesome-icon-list a').click(function(){
        let icon_name  = $(this).attr('href')
        let input_target = '<?= _GETS('target') ?>'
        
        $('#'+input_target).val('fa-'+icon_name)
        $('#'+input_target+'_preview').html('<i class="fa fa-'+icon_name+'"></i>')
        $.fancybox.close();
        return false;
    })
    
    $('#fa_search').on('input',function(){
        let keyword = $(this).val()
        let search_list = []
        
        if(keyword.length>0)
        {
           $('.fontawesome-icon-list .page-header').addClass('hidden') 
           
           $('.fontawesome-icon-list .fa-hover').each(function(){
               let text = $('a',$(this)).attr('href')
               if(text.search(new RegExp(keyword,'i'))!=-1 && !search_list.includes(text))
               {
                   $(this).removeClass('hidden')
                   search_list.push(text)
               }
               else
               {
                   $(this).addClass('hidden')
               }
           })
        }
        else
        {
            $('.fontawesome-icon-list .page-header').removeClass('hidden') 
            $('.fontawesome-icon-list .fa-hover').removeClass('hidden')
        }
            
    })
</script>